package com.inuker.bluetooth.library.model;

public class ChRecord {
    private int no;
    private int count;
    private String scriptureName;

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getScriptureName() {
        return scriptureName;
    }

    public void setScriptureName(String scriptureName) {
        this.scriptureName = scriptureName;
    }
}
